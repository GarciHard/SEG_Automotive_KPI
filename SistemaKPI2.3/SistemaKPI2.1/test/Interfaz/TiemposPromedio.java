    private void comboTurnosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboTurnosItemStateChanged
        Object[] datos;
        if (comboTurnos.getSelectedIndex() > 0) {
            datos = cls.buscaTiempoLiena(comboLineas.getSelectedItem().toString(), comboTurnos.getSelectedIndex());
            if (datos[0] != null) {
                txtNoPersonas.setText(datos[0].toString());
                txtDuracionTurno.setText(datos[1].toString());
                txtTcPromedio.setText(datos[2].toString());
                txtPzsHora.setText(datos[3].toString());
                float pzas = 0;
                if (!txtPzsHora.getText().equals("") && !txtDuracionTurno.getText().equals("")) {
                    pzas = Float.parseFloat(txtPzsHora.getText()) * Float.parseFloat(txtDuracionTurno.getText());
                    txtPzasPorTurno.setText(String.valueOf(pzas));
                }
            }
        }


    }//GEN-LAST:event_comboTurnosItemStateChanged


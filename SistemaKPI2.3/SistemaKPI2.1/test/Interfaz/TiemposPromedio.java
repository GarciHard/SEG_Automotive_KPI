    private void txtTcPromedioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTcPromedioKeyTyped
        char car = evt.getKeyChar();
        if (txtTcPromedio.getText().equals("")) {
            if ((car < '1' || car > '9')) {
                evt.consume();
            }
        } else {
            if ((car < '0' || car > '9')) {
                if ((car != '.')) {
                    evt.consume();
                }
            }
            if (txtTcPromedio.getText().length() >= 6) {
                evt.consume();
            }        // TODO add your handling code here:
            if (car == '.' && txtTcPromedio.getText().contains(".")) {
                evt.consume();
            }
        }
    }//GEN-LAST:event_txtTcPromedioKeyTyped


    private void txtTcPromedioCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtTcPromedioCaretUpdate
        float pzas = 0;

        if (!txtTcPromedio.getText().equals("") && !txtDuracionTurno.getText().equals("")) {
            pzas = 3600 / Float.parseFloat(txtTcPromedio.getText());
            txtPzsHora.setText(String.valueOf(pzas));
        }
        float pzas2 = 0;
        if (!txtPzsHora.getText().equals("") && !txtDuracionTurno.getText().equals("")) {
            pzas2 = Float.parseFloat(txtPzsHora.getText()) * Float.parseFloat(txtDuracionTurno.getText());
            txtPzasPorTurno.setText(String.valueOf(pzas2));
        }
    }//GEN-LAST:event_txtTcPromedioCaretUpdate


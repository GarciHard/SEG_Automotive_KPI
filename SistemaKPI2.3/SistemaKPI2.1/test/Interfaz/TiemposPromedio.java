    private void txtPzsHoraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPzsHoraKeyTyped
        char car = evt.getKeyChar();
        if ((car < '0' || car > '9')) {
            if ((car != '.')) {
                evt.consume();
            }
        }
        if (txtPzsHora.getText().length() >= 6) {
            evt.consume();
        }        // TODO add your handling code here:
        if (car == '.' && txtPzsHora.getText().contains(".")) {
            evt.consume();
        }
    }//GEN-LAST:event_txtPzsHoraKeyTyped

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (!txtNoPersonas.getText().equals("") && !txtPzsHora.getText().equals("") && !txtTcPromedio.getText().equals("")
                && comboTurnos.getSelectedIndex() != 0 && comboLineas.getSelectedIndex() != 0) {
            Object[] datos = new Object[7];
            datos[0] = comboLineas.getSelectedItem().toString();
            datos[1] = comboTurnos.getSelectedIndex();
            datos[2] = txtNoPersonas.getText();
            datos[3] = txtDuracionTurno.getText();
            datos[4] = txtTcPromedio.getText();
            datos[5] = txtPzsHora.getText();
            datos[6] = txtPzasPorTurno.getText();
            cls.actualizaTiemposLinea(datos);
            JOptionPane.showMessageDialog(this, "Actualizacion Correcta");
            comboLineas.setSelectedIndex(0);
            comboTurnos.setSelectedIndex(0);
            txtNoPersonas.setText("");
            txtDuracionTurno.setText("");
            txtTcPromedio.setText("");
            txtPzsHora.setText("");
            txtPzasPorTurno.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed


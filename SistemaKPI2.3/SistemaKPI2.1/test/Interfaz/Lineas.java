    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!txtCodLinea.getText().endsWith("") || !txtNombre.getText().equals("") || comboTipo.getSelectedIndex() != -1) {
            String[] datosL = new String[3];
            String t = "0" + comboTipo.getSelectedIndex();
            datosL[0] = txtCodLinea.getText();
            datosL[1] = t;
            datosL[2] = txtNombre.getText();
            if (btnAgregar.getText().equals("Modificar")) {
                PrincipalOne.in.updateLinea(this, datosL, l);
                model = PrincipalOne.ms.llenaTabla(model);
                txtCodLinea.setText("");
                txtNombre.setText("");
                btnAgregar.setText("Agregar");
                comboTipo.setSelectedIndex(-1);
            } else {
                if (!PrincipalOne.in.insertaLineas(this, datosL)) {
                    model = PrincipalOne.ms.llenaTabla(model);
                    txtCodLinea.setText("");
                    txtNombre.setText("");
                    comboTipo.setSelectedIndex(-1);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void menuEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuEditarActionPerformed
        if (tablaLineas.getSelectedRow() != -1) {
            int fila = tablaLineas.getSelectedRow();
            String linea;
            char cod;
            l = tablaLineas.getValueAt(fila, 0).toString();
            String[] datosL = PrincipalOne.ms.BuscaDato(l);
            txtCodLinea.setText(datosL[0]);
            txtNombre.setText(datosL[1]);
            cod = datosL[2].charAt(1);
            comboTipo.setSelectedIndex(Integer.parseInt(String.valueOf(cod)));
            btnAgregar.setText("Modificar");
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un campo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_menuEditarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        DefaultComboBoxModel mod = new DefaultComboBoxModel();
        for (int i = 0; i < PrincipalOne.cls.tipoEnsamble.size(); i++) {
            mod.addElement(PrincipalOne.cls.tipoEnsamble.get(i));
        }
        comboTipo.setModel(mod);
        model = (DefaultTableModel) tablaLineas.getModel();
        model = PrincipalOne.ms.llenaTabla(model);
        tablaLineas.setModel(model);
        comboTipo.setSelectedIndex(-1);

    }//GEN-LAST:event_formWindowOpened

    private void MenuEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuEliminarActionPerformed
        String linea;
        int fila = tablaLineas.getSelectedRow();
        linea = tablaLineas.getValueAt(fila, 0).toString();
        int op = JOptionPane.showConfirmDialog(this, "El registro se eliminara permanentemente\n"
                + "Â¿Desea continuar?", "Error", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (op == 0) {
            PrincipalOne.in.deleteLinea(linea);
            model = PrincipalOne.ms.llenaTabla(model);
        }
    }//GEN-LAST:event_MenuEliminarActionPerformed

